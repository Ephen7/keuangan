                         <h3>Sejarah Gereja Bethel Indonesia Anugerah Allah</h3>
                        <p align="justify" >Gereja Bethel Indonesia Anugerah Allah adalah sebuah gereja yang berada dalam Sinode Gereja Bethel Indonesia (GBI), yang adalah anggota dari Persekutuan Gereja-gereja di Indonesia (PGI), Dewan Pentakosta Indonesia (DPI), dan Persekutuan Injili Indonesia (PII).</p>
<p align="justify" >GBI Anugerah Allah digembalai oleh Pdt. Liem Tayen, S.Th. yang terletak di Mall Of Indonesia, Ruko City Home Blok M.3 SAN FRANSISCO BAY, RT.18/RW.8, Kelapa Gading Barat, Kecamatan Kelapa. Gading, Kota Jakarta Utara, Daerah Khusus Ibukota Jakarta 14240.</p>
<p align="justify" >GBI Anugerah Allah lahir dari sebuah pergumulan dan doa dari empat orang yang terbeban mendirikan sebuah gereja lokal untuk melaksanakan amanat agung Tuhan Yesus. Dengan pertolongan Tuhan  keempat pencetus sekaligus pendiri yaitu Pdt. Liem Tayen, S.Th., Pdt. Faonasokhi Lombu, S.Th., Martin Sujatmiko, ST., dan Jefri Lambert Pakpahan, SE., memulai ibadah perdana pada hari minggu, 3 Januari 2016 di rumah jemaat simpatisan yang terletak di Jl. Sunter Agung Barat 16 Blok B14 no. 5 Sunter Podomoro, Jakarta Utara, dengan anggota dewasa 12 - 15 orang yang sudah dibabtis dan beberapa pemuda remaja serta anak Sekolah Minggu. Ditabiskan menjadi suatu jemaat Gereja Bethel Indonesia pada tanggal 18 Desember 2016 yang beralamat di Hotel Kawanua,Â Jl. Cempaka Putih Raya No.120, RT.8/RW.3, Cempaka Putih Timur, Kecamatan. Cempaka. Putih, Kota Jakarta Pusat, Daerah Khusus Ibukota Jakarta 10510, pada saat itu jemaat di GBI Anugerah Allah mengalami peningkatan jemaat hingga 20-25 jemaat dewasa.</p> 
<p align="justify" >Pada tahun 2017 akhir GBI Anugerah Allah pindah ke Mall Of Indonesia, Ruko City Home Blok M.3 SAN FRANSISCO BAY, RT.18/RW.8, Kelapa Gading, karena peningkatan jemaat dan tidak memadainya tempat ibadah. Dan pada saat ini jemaat sudah mencapai 70-80 orang.
